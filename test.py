def caesar_code(text, lang, k):
    res = []
    if lang == 'eng':
        alphabet = ALPHABET_ENG
    else:
        alphabet = ALPHABET_RUS

    for ch in text:
        low = ch.lower()
        if low in alphabet:
            idx = alphabet.index(low)
            new_idx = (idx + k) % len(alphabet)
            new_ch = alphabet[new_idx]
            res.append(new_ch.upper() if ch.isupper() else new_ch)
        else:
            res.append(ch)
    return ''.join(res)

